    private void actualizarValoresA()
    {
        if(cbBase.getSelectedItem().equals(cbDestino.getSelectedItem()))
        {
            int temp = actualBase;
            actualBase = actualDestino;
            actualDestino = temp;
            cbBase.setSelectedIndex(actualDestino);
            cbDestino.setSelectedIndex(actualBase);
            
        }
    }
